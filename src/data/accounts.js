export const formatCurrency = (value, opts = {}) => {
  const { signed = false } = opts;
  const abs = Math.abs(value).toLocaleString('en-US', {
    style: 'currency',
    currency: 'USD',
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  });
  if (signed) {
    return `${value < 0 ? '−' : '+'}${abs}`;
  }
  return value < 0 ? `−${abs}` : abs;
};
